//like
function likeone() {
    if ((document.getElementById("heartone").name) == "heart-outline") {
        document.getElementById("heartone").name = "heart";
        document.getElementById("heartone").style.color = "red";
    }
    else {
        document.getElementById("heartone").name = "heart-outline";
        document.getElementById("heartone").style.color = "black";
    }
}


